"use strict";

function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function divide(a, b) {
    return a / b;
}

function multiply(a, b) {
    return a * b;
}

console.log(add(123, -1234)); // -1111
console.log(subtract(1000, 7)); // 993
console.log(divide(123214, 6)); // 20535.666666666668
console.log(multiply(123, 321)); // 39483